export type UiLanguage = "en" | "ru";

export function resolveUiLanguage(language: string): UiLanguage {
  if (language === "ru") {
    return "ru";
  }
  if (language === "system" && typeof navigator !== "undefined" && navigator.language.toLowerCase().startsWith("ru")) {
    return "ru";
  }
  return "en";
}

